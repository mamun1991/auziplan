            <h5>Who are Company Owner?</h5>   
                <p class="mt-1 ps-3 mb-3">
                  A Owner of a company is a person who is usually responsible for managing the companies business activities. In many cases they appoint themselves as Directors, and sit on the board of directors.	
                </p>  	   
                <p class="mt-1 ps-3 mb-3">
                  In the vast majority of cases when considering how to fund a business, the owners and management team can only contribute a small percentage of the funds required to start the business
                  and outside help is usually required. This could be in the form of bank finance or investors.
                </p>  	 
                <p class="mt-1 ps-3 mb-3">
                  There are different types and sources of finance available, but generally they fall into one of two categories, <b>Equity Finance</b> or <b>Debt Finance</b>.
                </p>  	  
                <p class="mt-1 ps-3 mb-3">
                <b>Equity Finance</b> includes capital injected by the owners of the company or outside investors, such as angel investors, venture capital investors, or the stock markets. 
                  Any retained profits of the business are also classified as equity.
                </p>  

                <p class="mt-1 ps-3 mb-3">
                  <b>Debt Finance</b> on the other hand includes loans from family members and friends, bank loans and overdrafts, government loans, mortgages, and leasing.
                  Each comes with their owen costs and obligations.


                </p>  			
