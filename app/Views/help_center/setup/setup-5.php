 <h2>4. What type of company are you setting up?</h2>


    <p class="pb-3">The four most common business structures available to startups are: sole trader, partnership, company and trust. 
    Out of all four options, setting up a startup as a company provides you with the greatest amount of flexibility and options to expand, offer equity, raise capital and hire employees.
    </p>

