   <h2>5. What are your business trading terms?</h2>
        <div class="row p-5">          
            <ol class=" mt-3">                                            
                <li class="pb-3">
                <p> <b>Aged Receivable's:</b>  Accounts receivables arise when the business provides goods and services on a credit to the clients. 
                 For example, you may allow clients to pay for goods 30 days after they are delivered.</p>                                                        
                </li>
                <li class="pb-3">
                <p><b>Aged Payable's:</b>Accounts payable aging report helps you figure how well you are paying your bills and helps you prioritize payments. 
                    For example, you may ask for 30 Day credit with your suppliers.</p>                
                </li>
                <li class="pb-3">
                <p>
                <b>BAS (Business Activity Statement):</b>If you are a business registered for GST you need to lodge a business activity statement (BAS).
                The BAS Statement is usually reported and paid quarterly, however we recommend setting aside your payments on a monthly basis
                so your always ahead.
                </p>   
                <p>Your BAS will help you report and pay your:</p>
                    <ul>
                        <li>goods and services tax (GST)</li>
                        <li>pay as you go (PAYG) instalments</li>
                        <li>PAYG withholding tax</li>
                        <li>other taxes.</li>
                    </ul>
                </li>
            <li class="pb-3">
            <p><b>Company Tax:</b> Normally your accountant will reconcile your business at the end of your financial year , 
                and your taxes are due at the end of the year, however we recommend setting aside your tax on a monthly basis
                so your always ahead..</p>                
            </li>
