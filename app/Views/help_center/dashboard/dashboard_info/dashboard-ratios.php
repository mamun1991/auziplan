     <h2>What are Business Key Ratios?</h2>
        <hr style="border-top: 3px solid #0064B2; background: transparent;">    
         <p class="text-left">   
          Key ratios are the primary financial ratios used to illustrate and summarize the current financial condition of a company.                
          These are produced by comparing different line items from the subject firm's financial statements.                   
          Analysts and investors use key ratios to see how companies stack up against their peers.          
         </p>
          <hr style="border-top: 3px solid #0064B2; background: transparent;">    


        <dl class="row">                     
            <h5>Profitability Ratios</h5>

            <dt class="col-sm-3">Net Profit Percentage</dt>
            <dd class="col-sm-9">Net profit as a percentage of total income is a key financial ratio.
            As the business grows, the net profit percentage should increase to reflect the efficiency derived from fixed costs and increasing revenue.
            A falling net profit percentage is cause for concern as either gross profit margins are falling or costs are increasing faster than income.</dd>

            <dt class="col-sm-3">Employment Costs</dt>
            <dd class="col-sm-9">
              <p>Employment Costs as a percentage of total income. 
                As employment costs are usually the highest expense category it is vital that the business keeps these costs in line with total income
                As the business grows, there should be a leveraging effect that will allow the employment costs percentage to decrease. </p>
            </dd>
            <dt class="col-sm-3">Business Development</dt>
            <dd class="col-sm-9">Business development expenditure is shown as a percentage of total income.
              Obviously this is high in the early years as the business targets increased market share. 
              In later years, the benefits of the business development expenditure should be evident in higher income and the percentage of new expenditure can be lower.</dd>
             <hr style="border-top: 3px solid #0064B2; background: transparent;">    

            <h5>Balance Sheet Ratios</h5>

            <dt class="col-sm-3">Earnings Per Share</dt>
            <dd class="col-sm-9">Earnings per share calculates the net profit after tax and divides by the number of shares issues to obtain a net profit per share.</dd>

            <dt class="col-sm-3">Working Capital Ratio</dt>
            <dd class="col-sm-9">
              <p>Working capital reflect the relationship between current assets and current liabilities. 
                It is health check on the business and should always be greater than one (preferably greater than 1.5). 
                A ratio of one or greater suggest that the business has sufficient current assets to meet its current liabilities.</p>

            </dd>
            <dt class="col-sm-3">Debtors Turnover</dt>
            <dd class="col-sm-9">The debtors turnover ratio calculates an average number of times the debtors are turned over in a year. 
                It is calculated in a simple format by dividing the sales by the level of debtors at the end of the year. More exact measures are available, but this method provided a general guide.
                The debtors should turnover at least 6 times per year suggesting that average collection times are 2 months. Note that in a fast growth business where the sales increase toward the end of the year, the ratio will be skewed due to the higher debtors at the end of the year. </dd>

        </dl>
