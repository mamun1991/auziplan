                <h5>3.  BOM (Bill of Materials)</h5>
            
        

                <h5>What is BOM?</h5>
                    
                <p>BOM is the sum of direct costs required to produce a product. Direct costs include:</P>

                <ul>
                    <li>materials;</li>
                    <li>labour; </li>
                    <li>transaction costs; and sometimes </li>
                    <li>a portion of your fixed costs (like hosting fees for a business that sells software). </li>
                    </ul>
                
                    <p>
                        It can be useful to calculate the average cost to produce your products. 
                        This tells you the cost to produce one unit of your product, also known as your marginal cost.
                        Your marginal cost can be used to calculate your gross margin.
                    </p>
                    
                    <h5 class="pt-1">Key Takeaways</h5>
                    <p>
                        Accurately measuring your COGS allows you to calculate your gross profit margin. Understanding your gross margin provides a 
                        powerful tool for assessing the potential of your business to grow profitably. 
                        Businesses should work to reduce their COGS before pouring money into sales and marketing. 
                        This will ensure that you get to keep a larger proportion of the revenues you collect from new customers.
                    </p>
           
            
                    <h5>4.  Markup Summary</h5>

 
        
                    <p>
                        The price you charge for your product can be the difference between success or failure. Early stage businesses have slim (and often negative) margins. 
                        A small percentage change in price can be the difference between reaching your goal and objectives or simply running out of cash .
                    </p>
                    
                    <p>
                        Markup is the ratio between the cost of a goods or services and its selling price. 
                        A % markup is added onto the total cost incurred by the product or service in order to cover the costs of goods or services ,
                        and operating expenses in order to create a profit.
                    </p>

                    <h5 class="pt-1">Key Takeaways</h5>
                    <p>
                        Pricing is as important to your business as it is a key ingredient in generating recurring sales. Your pricing should reflect your product, your customers, 
                        your cost structure and your competitors.
                        Reviewing your pricing on a regular, as well as comparing your markup to your competitor's can help you in finding right price for your product.
                    </p>

