        <h4 class="card-text">What are trade creditors?</h4>  
            <p>
            A trade credit is a business-to-business (B2B) agreement in which a customer can purchase goods on account without paying cash up front, paying the supplier at a later scheduled date. 
            Usually, businesses that operate with trade credits will give buyers 30, 60, or 90 days to pay, with the transaction recorded through an invoice.
            </p>
       
            
