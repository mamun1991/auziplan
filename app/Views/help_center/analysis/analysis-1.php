        <h4 class="card-text">What is current assets?</h4>  
            <p>
            Current assets represent all the assets of a company that are expected to be conveniently sold, consumed, used, or exhausted through standard business operations with one year. 
            Current assets appear on a company's balance sheet, one of the required financial statements that must be completed each year.</p>
            <p>Current assets would include cash, cash equivalents, accounts receivable, stock inventory, marketable securities, pre-paid liabilities, and other liquid assets. 
               Current assets may also be called current accounts.
            </p>
