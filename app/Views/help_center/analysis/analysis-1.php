        <h4 class="card-text">What Are Current Liabilities?</h4>  
            <p>
            Current liabilities are a company's short-term financial obligations that are due within one year or within a normal operating cycle. 
            An operating cycle, also referred to as the cash conversion cycle, is the time it takes a company to purchase inventory and convert it to cash from sales. 
            An example of a current liability is money owed to suppliers in the form of accounts payable.
            </p>
       
            
        <hr style="border-top: 3px solid #0064B2; background: transparent;">    
        <h4 class="card-text">What Is Days Working Capital?</h4>  
            <p>
            Working capital, also known as net working capital, is the difference between a company’s current assets, like cash, accounts receivable, 
            and inventories of raw materials and finished goods, and its current liabilities, like accounts payable and the current portion of debt due within one year.
            </p>
            
            
        <hr style="border-top: 3px solid #0064B2; background: transparent;">    
        <h4 class="card-text">What are Non-Current Assets?</h4>  
            <p>
            Non-current assets are assets that are not turned into cash easily, are expected to be turned into cash within a year, and/or have a lifespan of more than a year. 
            They can refer to tangible assets, such as machinery, computers, buildings and land. Non-current assets also can be intangible assets, such as goodwill, patents or copyright. 
            While these assets are not physical in nature, they are often the resources that can make or break a company—the value of a brand name, for instance, should not be underestimated.
            </p>
            
            
            
            
                 
