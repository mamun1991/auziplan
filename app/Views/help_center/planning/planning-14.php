 <p class="mb-0 ps-1">			
	S.W.O.T. stands for strengths, weaknesses, opportunities and threats, it helps to fine tune your business strategy.
	</p>	
	<p class="mb-0 ps-1">			
	The example below will help you to identify each of these for your business, so that you can conduct regular S.W.O.T Analysis to keep your business on the right track.
	</p>	

		<p class="pt-1">
			<b>What is a S.W.O.T analysis? </b>
		
		</p>
		<ul class="ml-3">
			<li>
				Usually strengths and weaknesses are internal aspects of your business (what are you good at, what are you not so good at), 
				while opportunities and threats are external forces (what is likely to impact on your business in a negative or positive way)
			</li>

			<li>
				Strengths – top strengths or benefits of your business, and how you can protect and enhance them.
			</li>

			<li>
				Weaknesses – your top weaknesses or issues, and how you can minimize them or do better.
			</li>
		<li>
				Opportunities – top opportunities for your business and how you can access and take advantage of them.
			</li>
		
			<li>
				Threats – your top threats, and how you can minimize them and do better.
			</li>

		</ul>

