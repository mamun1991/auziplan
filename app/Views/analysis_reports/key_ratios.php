                                                    <h5>What Are Key Ratios?</h5>
                                                    <p>
                                                        A Key ratio is a mathematical ratio that illustrates and summarizes the current financial condition of a company. 
                                                        Key ratios can be used to easily obtain an idea of a company's financial status. 
                                                        Companies that are in good condition financially will have superior ratios to those that are performing poorly.
                                                    </p>

                                                    <h5>Gross profit margin ratio.
                                                        
                                                    </h5>

                                                    <p>
                                                        Gross profit marging = Gross Profoit / Sales.
                                                    </p>
                                                    <p>
                                                        It is an excellent way of assessing the profitability of each product.
                                                    </p>

                                                    <h5>
                                                        Net profit margin ratio.
                                                    </h5>

                                                    <p>
                                                        Net profit marging = Net Income / Sales.
                                                    </p>
                                                    <p>
                                                        The percentage profit your business makes for every dollar of revenue.
                                                    </p>
                                                    <p>
                                                        It will be partly determined by your industry ­– some retailers run high-volume, low-margin businesses,
                                                        others sell a few expensive items with plenty of margin built in.                                                   
                                                    </p>

                                                    <h5>Current ratio.</h5>

                                                    <p>Current ratio = Current assets / Current liabilities.</p>
                                                    <p>Helps measure the solvency of your business by comparing current assets (like unpaid invoices) to current liabilities (unpaid bills)</p>
                                                    <p>Ideally, your current ratio should be two or more, which means your assets are at least double your liabilities</p>

                                                    <h5>Inventory turnover ratio.</h5>

                                                    <p>
                                                    Inventory turnover = Cost of goods sold / Inventory.
                                                    </p>
                                                    <p>
                                                        For example, if you’ve spent $200,000 on stock over the year and you keep an average of $20,000 worth of stock on hand, 
                                                        your inventory turnover is 10 times a year.
                                                    </p>


                                                    <h5>Return on owner’s equity.</h5>

                                                    <p>Return on owner’s equity = Net Income / owner’s equity.</p>
                                                    <p>
                                                        For example, if you’ve invested $200,000 and the business is generating a net income of $100,000 a year, 
                                                        your return on owner’s equity is 50 per cent.
                                                    </p>

                                                    <p>
                                                        There are actually several different key ratios used by analysts to examine a companies financial condition. 
                                                        These include the capital to assets ratio, the loan loss reserves to total loans ratio, 
                                                        the liquidity ratio and many others. These ratios provide direct measures of different specific aspects of a comanies assets, 
                                                        liabilities and cash flow.
                                                    </p>

